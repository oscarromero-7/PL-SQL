CREATE TABLE TAB_CLIENTE OF T_CLIENTE;
/
DECLARE
	T_CLI T_CLIENTE;
BEGIN
	FOR REG IN (SELECT COALESCE(CEDULA,RUC) DOCUMENTO FROM B_PERSONAS WHERE ES_CLIENTE = 'S') LOOP
		T_CLI := T_CLIENTE.ASIGNAR_CLIENTE(REG.DOCUMENTO);
		INSERT INTO TAB_CLIENTE VALUES(T_CLI);
	END LOOP;
	COMMIT;
END;
/

SELECT CEDULA_RUC, T.NOMBRE_COMPUESTO() FROM TAB_CLIENTE T;