SET SERVEROUTPUT ON
ACCEPT VALOR_INGRESADO CHAR PROMPT 'Ingrese un valor: '
DECLARE
	VALOR VARCHAR2(20);
	VALOR_INTERCAMBIADO VARCHAR2(20);	
BEGIN
	VALOR := '&VALOR_INGRESADO';
	DBMS_OUTPUT.PUT_LINE('Valor ingresado: ' || VALOR);
	SELECT REVERSE(VALOR) 
	INTO VALOR_INTERCAMBIADO
	FROM DUAL;
	IF VALOR = VALOR_INTERCAMBIADO THEN
		DBMS_OUTPUT.PUT_LINE('El valor es palindromo: ' || VALOR_INTERCAMBIADO);
	ELSE
		DBMS_OUTPUT.PUT_LINE('El valor NO es palindromo: ' || VALOR_INTERCAMBIADO);
	END IF;
END;
