SET SERVEROUTPUT ON
ACCEPT VALOR_INGRESADO CHAR PROMPT 'Ingrese un valor: ';
DECLARE
	VALOR VARCHAR2(20);
	--CONTADOR NUMBER(2);
	VALOR_INTERCAMBIADO VARCHAR2(20);
BEGIN
	VALOR := '&VALOR_INGRESADO';
	DBMS_OUTPUT.PUT_LINE('Valor ingresado: ' || VALOR);
	SELECT REVERSE(VALOR) INTO VALOR_INTERCAMBIADO
	FROM DUAL;
	DBMS_OUTPUT.PUT_LINE('Valor intercambiado: ' || VALOR_INTERCAMBIADO);
END;
