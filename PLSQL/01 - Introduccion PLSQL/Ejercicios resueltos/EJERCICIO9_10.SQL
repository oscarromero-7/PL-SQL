SET SERVEROUTPUT ON
ACCEPT A NUMBER PROMPT 'Ingrese A: ';
ACCEPT B NUMBER PROMPT 'Ingrese B: ';
ACCEPT C NUMBER PROMPT 'Ingrese C: ';
DECLARE
	VALOR_A NUMBER(3);
	VALOR_B NUMBER(3);
	VALOR_C NUMBER(3);
	DISCRIMINANTE NUMBER(6);
	FORMULA NUMBER(3,2);
BEGIN
	VALOR_A := &A;
	VALOR_B := &B;
	VALOR_C := &C;
	DISCRIMINANTE := VALOR_B ** 2 - (4 * VALOR_A * VALOR_C);
	CASE
	WHEN DISCRIMINANTE < 0 THEN
		DBMS_OUTPUT.PUT_LINE('No tiene solucion en el conjunto de los numeros reales.');
	WHEN DISCRIMINANTE = 0 THEN
		FORMULA := (-VALOR_B) / (2 * VALOR_A);
		DBMS_OUTPUT.PUT_LINE('X1 = X2 = '|| FORMULA);
	WHEN DISCRIMINANTE > 0 THEN
		FORMULA := (-VALOR_B + SQRT(DISCRIMINANTE)) / (2 * VALOR_A);
		DBMS_OUTPUT.PUT_LINE('X1 = '|| FORMULA);
		FORMULA := (-VALOR_B - SQRT(DISCRIMINANTE)) / (2 * VALOR_A);
		DBMS_OUTPUT.PUT_LINE('X2 = '|| FORMULA);
	END CASE;
END;
/
