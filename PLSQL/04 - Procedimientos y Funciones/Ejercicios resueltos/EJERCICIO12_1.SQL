CREATE OR REPLACE FUNCTION F_OBTENER_SALARIO (INGRESADA_CEDULA IN NUMBER) RETURN NUMBER IS
		V_SALARIO B_CATEGORIAS_SALARIALES.ASIGNACION%TYPE := 0;
	BEGIN
		V_SALARIO := 0;
		SELECT CS.ASIGNACION
		INTO V_SALARIO
		FROM B_EMPLEADOS E
		INNER JOIN B_POSICION_ACTUAL PA ON E.CEDULA = PA.CEDULA
		INNER JOIN B_CATEGORIAS_SALARIALES CS ON PA.COD_CATEGORIA = CS.COD_CATEGORIA
		WHERE PA.FECHA_FIN IS NULL AND E.CEDULA = INGRESADA_CEDULA;
		RETURN V_SALARIO;
	EXCEPTION
		WHEN NO_DATA_FOUND THEN
			-- Manejo de excepción cuando no se encuentra ningún dato
			DBMS_OUTPUT.PUT_LINE('No se encontró información salarial para el empleado con cédula ' || INGRESADA_CEDULA);
			RETURN 0; -- O el valor que desees en este caso
		WHEN OTHERS THEN
			-- Manejo de otras excepciones
			RAISE_APPLICATION_ERROR(-20001,'Error: ' || SQLCODE || ' - ' || SQLERRM);
			RETURN 0; -- O el valor que desees en este caso
	END;
